
class Node:
    def __init__(self, val, left=None, right=None) -> None:
        self.val = val 
        self.left = left 
        self.right = right 

def tree_includes(root, target):
    if root is None:
        return False 

    if root.val == target:
        return True 

    return tree_includes(root.left, target) or tree_includes(root.right, target)

